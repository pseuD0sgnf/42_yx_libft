#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*a1;
	unsigned char	*a2;

	i = 0;
	a1 = (unsigned char *)s1;
	a2 = (unsigned char *)s2;
	while (a1[i] == a2[i] && i < n)
		i++;
	if (i == n)
		return (0);
	return ((unsigned char)a1[i] - (unsigned char)a2[i]);
}
